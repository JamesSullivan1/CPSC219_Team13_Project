/**
 * This class represents our player which owns the pieces, can win, can loose,
 * and can quit.
 * 
 * @author Zsanett
 * 
 */
public class Player {

	/**
	 * Each player owns 12 pieces.
	 */

	public Player(int colour) {
		if (colour == Piece.RED){
			Piece[] pieces = new Piece[12];
		}
		else if (colour == Piece.BLACK){
			Piece[] pieces = new Piece[12];
		}
	}
}

	
